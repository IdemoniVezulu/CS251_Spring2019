//This class is for maintaining paired item for problem 1 and 2

public class Pair {
    //Declare necessary variables and methods
    private int a;
    private int b;

    public void setA(int value) {
        this.a = value;
    }

    public void setB(int value) {
        this.b = value;
    }

    public int getA() {
        return a;
    }

    public int getB() {
        return b;
    }
}
